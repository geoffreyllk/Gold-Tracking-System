INSERT INTO purity_prices (gold_price_id, purity, price_per_gram)
SELECT 
    gp.id,
    purities.purity,
    ROUND((gp.price / 31.1035) * (purities.purity_value/24), 4) as price_per_gram
FROM gold_prices gp
CROSS JOIN (
    SELECT '24k' as purity, 24 as purity_value UNION ALL
    SELECT '22k', 22 UNION ALL
    SELECT '21k', 21 UNION ALL
    SELECT '20k', 20 UNION ALL
    SELECT '18k', 18 UNION ALL
    SELECT '16k', 16 UNION ALL
    SELECT '14k', 14 UNION ALL
    SELECT '10k', 10
) purities
WHERE gp.price_date BETWEEN '2025-12-10' AND '2025-12-12';